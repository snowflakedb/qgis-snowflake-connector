    def _targets_same_snowflake_table(
        self,
        parameters,
        context,
        selected_connection: str,
        selected_database: str,
        selected_schema: str,
        selected_table: str,
    ) -> bool:
        """Return True when the chosen destination matches the input layer's
        source Snowflake table. Running Export on the same table duplicates
        every row, since the algorithm is INSERT-only.
        """
        try:
            layer = self.parameterAsVectorLayer(parameters, self.INPUT, context)
            if layer is None:
                return False
            provider = layer.dataProvider()
            if provider is None or provider.name() != "snowflakedb":
                return False
            (
                src_connection,
                _sql_query,
                src_schema,
                src_table,
                _srid,
                _geom_column,
                _geometry_type,
                _geo_column_type,
                _primary_key,
                _load_all_rows,
            ) = parse_uri(provider.dataSourceUri())
        except Exception:
            return False

        src_auth = get_authentification_information(self.settings, src_connection)
        src_database = src_auth.get("database", "") if src_auth else ""

        dst_schema = selected_schema if selected_schema else "PUBLIC"
        src_schema_norm = src_schema if src_schema else "PUBLIC"

        def _eq(a, b):
            return (a or "").strip().upper() == (b or "").strip().upper()

        return (
            _eq(src_connection, selected_connection)
            and _eq(src_database, selected_database)
            and _eq(src_schema_norm, dst_schema)
            and _eq(src_table, selected_table)
        )

    def checkParameterValues(
        self, parameters: typing.Dict[str, typing.Any], context: "QgsProcessingContext"
    ) -> typing.Tuple[bool, str]:
        """
        Check if the parameters are valid and return a tuple with a boolean and a message
        """
        try:
            geom_column = self.parameterAsString(
                parameters, self.GEOMETRY_COLUMN, context
            )
            selected_connection, selected_database, selected_schema, selected_table = (
                json.loads(
                    self.parameterAsString(parameters, self.CONNECTION_DYN_CB, context)
                )
            )
            selected_table_is_empty = False
            if selected_schema == "":
                selected_schema = "PUBLIC"
            if selected_table == "":
                selected_table_is_empty = True
                selected_table = self.parameterAsSource(
                    parameters, self.INPUT, context
                ).sourceName()
            if geom_column == "":
                return False, "Geometry Column can not be empty!"

            if selected_connection == "":
                return False, "Please select a connection!"

            if self._targets_same_snowflake_table(
                parameters,
                context,
                selected_connection,
                selected_database,
                selected_schema,
                selected_table,
            ):
                return False, SAME_TABLE_EXPORT_MESSAGE

            if not selected_table_is_empty:
                # Check if the selected table exists
                source = self.parameterAsSource(parameters, self.INPUT, context)
                count_table = get_count_tables(
                    connection_name=selected_connection,
                    database_name=selected_database,
                    schema_name=selected_schema,
                    table_name=selected_table,
                )
                if count_table == 0:
                    query_create_table = self.get_create_table_query(
                        geom_column=geom_column,
                        source=source,
                        is_snowflake_layer=False,
                        database_name=selected_database,
                        schema_name=selected_schema,
                        table_name=selected_table,
                        use_geometry_type=source.sourceCrs().postgisSrid() != 4326,
                    )

                    create_table(
                        connection_name=selected_connection, query=query_create_table
                    )

                auth_information = get_authentification_information(
                    self.settings, selected_connection
                )
                self.sf_data_provider = SFDataProvider(auth_information)

                query_select_columns = f"""
                    SELECT DISTINCT COLUMN_NAME
                    FROM INFORMATION_SCHEMA.COLUMNS
                    WHERE TABLE_CATALOG ILIKE {quote_literal(selected_database)}
                    AND TABLE_SCHEMA ILIKE {quote_literal(selected_schema)}
                    AND TABLE_NAME ILIKE {quote_literal(selected_table)}
                """  # nosec B608 - values escaped via quote_literal
                cur_select_columns = self.sf_data_provider.execute_query(
                    query_select_columns, selected_connection
                )
                available_columns = []
                column_found = False
                for row in cur_select_columns.fetchall():
                    if row[0].upper() == geom_column.upper():
                        cur_select_columns.close()
                        column_found = True
                        return True, ""
                    else:
                        available_columns.append(row[0])

                if not column_found:
                    cur_select_columns.close()
                    return (
                        False,
                        f"Given Geometry Column: {geom_column} does not exist! Available columns: {', '.join(available_columns)}",
                    )
                cur_select_columns.close()

            return True, ""
        except Exception as e:
            return (
                False,
                f"There was an error while checking the parameter values. Error: {str(e)}",
            )

    def get_create_table_query(
        self,
        geom_column: str,
        source: "QgsVectorLayer",
        is_snowflake_layer: bool,
        database_name: str,
        schema_name: str,
        table_name: str,
        use_geometry_type: bool = False,
    ) -> str:
        """
        Generates a SQL query string to create a table in Snowflake with the specified columns and types.

        Args:
            geom_column (str): The name of the geometry column.
            source (QgsVectorLayer): The source vector layer containing the fields.
            is_snowflake_layer (bool): Flag indicating if the source is a Snowflake layer.
            database_name (str): The name of the database.
            schema_name (str): The name of the schema.
            table_name (str): The name of the table to be created.

        Returns:
            str: A SQL query string to create the table with the specified columns and types.
        """
        geom_data_type = "GEOMETRY" if use_geometry_type else "GEOGRAPHY"
        query_create_table_cols = f"{quote_identifier(geom_column)} {geom_data_type}"
        for field in source.fields():
            if field.name().lower() == geom_column.lower():
                continue
            if is_snowflake_layer:
                field_to_use = field.subType()
            else:
                field_to_use = field.type()

            query_create_table_cols += (
                f",{quote_identifier(field.name())} {self.get_field_type_from_code_type(field_to_use)}"
            )

        fq_table = qualified_table_name(database_name, schema_name, table_name)
        return f"CREATE TABLE {fq_table}({query_create_table_cols})"

    def get_geometry_insert_sql(
        self, hex_string: str, use_geometry_type: bool, source_srid: int
    ) -> str:
        if hex_string == "":
            return "NULL"
        if use_geometry_type:
            return (
                f"ST_SETSRID(ST_GEOMETRYFROMWKB(TO_BINARY('{hex_string}', 'HEX')),"
                f" {source_srid})"
            )
        return f"ST_GEOGFROMWKB(TO_BINARY('{hex_string}', 'HEX'))"
